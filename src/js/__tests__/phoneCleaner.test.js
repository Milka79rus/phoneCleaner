import cleanPhone from '../phoneCleaner';

test.each([
    ['8 (927) 000-00-00', '+79270000000'], // РФ через 8
    ['+7 960 000 00 00', '+79600000000'], // РФ через +7 с пробелами
    ['+86 000 000 0000', '+860000000000'], // Китай
    ['89270000000', '+79270000000'],       // РФ без скобок
    ['79270000000', '+79270000000'],       // РФ через 7 без плюса
])('очистка номера %s должна давать %s', (phone, expected) => {
    expect(cleanPhone(phone)).toBe(expected);
});